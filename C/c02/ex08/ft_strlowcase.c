char	*ft_strlowcase(char *str)
{
	int		i;
	char	now;

	i = 0;
	while (str[i] != '\0')
	{
		now = str[i];
		if (now >= 'A' && now <= 'Z')
		{
			str[i] = now + 32;
		}
		i++;
	}
	return (str);
}
