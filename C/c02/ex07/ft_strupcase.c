char	*ft_strupcase(char *str)
{
	int		i;
	char	now;

	i = 0;
	while (str[i] != '\0')
	{
		now = str[i];
		if (now >= 'a' && now <= 'z')
			str[i] = now - 32;
		i++;
	}
	return (str);
}
