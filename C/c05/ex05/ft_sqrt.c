int	ft_sqrt(int nb)
{
	unsigned int	i;

	if (nb < 0)
		return (0);
	if (nb <= 1)
		return (nb);
	i = 0;
	while ((i * i) < nb)
		i++;
	return ((i * i) == nb ? i : 0);
}
